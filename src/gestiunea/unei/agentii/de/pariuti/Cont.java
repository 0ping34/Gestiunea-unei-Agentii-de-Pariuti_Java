    private void Ad3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Ad3ActionPerformed
         String sql ="INSERT INTO cota(CODC,Miza,Castiguri,Profit,Sansa,CODPA) VALUES (?,?,?,?,?,?)";
        float s=Float.parseFloat(Sansa.getText());
        float m=Float.parseFloat(Miza.getText());
        float c,p;
        c=s*m;
        p=(s*m)-m;
        String castig=""+c;
        String profit=""+p;
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, Codc.getText() );
            pst.setString(2, Miza.getText() );
            pst.setString(3, castig );
            pst.setString(4, profit );
            pst.setString(5,Sansa.getText() );
            pst.setString(6, Codpaf.getText() );
            pst.execute();
            JOptionPane.showMessageDialog(null,"Inserat cu succes");
            UpdateTable4();
            ClearFields4();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_Ad3ActionPerformed

